export const STAGES: StageData[] = [...STAGES_BASE, ...SINGLE, ...VERSUS];

export function getStage(id: string): StageData {
  return STAGES.find((s) => s.id === id) ?? STAGES[0];
}

export function getStagesForMode(mode: StageMode): StageData[] {
  if (mode === 'single') {
    return STAGES.filter((s) => !s.mode || s.mode === 'single');
  }
  if (mode === 'versus') {
    return STAGES.filter((s) => !s.mode || s.mode === 'versus');
  }
  return STAGES;
}
