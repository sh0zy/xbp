export const STARTER_EQUIPMENTS: EquipmentId[] = ['none', 'triangle', 'pencil', 'eraserBig'];

export function getEquip(id: EquipmentId): EquipmentData {
  return EQUIPMENTS[id];
}

/** 装備配列から合成ステータスを計算 */
export function composeStats(baseRadius: number, baseMass: number, eq: EquipmentId[]) {
  let radius = baseRadius;
  let mass = baseMass;
  let power = 1;
  let friction = 1;
  let bounce = 1;
  let trick = 1;
  for (const id of eq) {
    const e = EQUIPMENTS[id];
    if (!e) continue;
    radius *= e.shapeModifier;
    mass *= e.weightModifier;
    power *= e.powerModifier;
    friction *= e.stabilityModifier;
    bounce *= e.collisionModifier;
    trick *= e.trickModifier;
  }
  return { radius, mass, power, friction, bounce, trick };
}
